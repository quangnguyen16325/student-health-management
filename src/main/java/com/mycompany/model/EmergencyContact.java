package com.mycompany.model;

public class EmergencyContact {
    private String studentCode;
    private String fullNameContact;
    private String relationshipContact;
    private String phoneContact;
    private String addressContact;

    public String getStudentCode() {
        return studentCode;
    }

    public void setStudentCode(String studentCode) {
        this.studentCode = studentCode;
    }

    public String getFullNameContact() {
        return fullNameContact;
    }

    public void setFullNameContact(String fullNameContact) {
        this.fullNameContact = fullNameContact;
    }

    public String getRelationshipContact() {
        return relationshipContact;
    }

    public void setRelationshipContact(String relationshipContact) {
        this.relationshipContact = relationshipContact;
    }

    public String getPhoneContact() {
        return phoneContact;
    }

    public void setPhoneContact(String phoneContact) {
        this.phoneContact = phoneContact;
    }

    public String getAddressContact() {
        return addressContact;
    }

    public void setAddressContact(String addressContact) {
        this.addressContact = addressContact;
    }
    
    
}
